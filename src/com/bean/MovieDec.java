package com.bean;

public class MovieDec {
		private Integer id;
		private Integer mid;
		private String decs;
		private float score;
		
		
		public Integer getId() {
			return id;
		}
		public void setId(Integer id) {
			this.id = id;
		}
		public Integer getMid() {
			return mid;
		}
		public void setMid(Integer mid) {
			this.mid = mid;
		}
		public String getDecs() {
			return decs;
		}
		public void setDecs(String decs) {
			this.decs = decs;
		}
		public float getScore() {
			return score;
		}
		public void setScore(float score) {
			this.score = score;
		}
		
}
